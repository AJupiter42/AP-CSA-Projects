
public class Problem18 {
	
	public static void main(String[] args) {
		
		// Rectangle:
		
		System.out.println("*********");
		
		for (int i = 0; i < 7; i++) {
			
			System.out.println("*       *");
			
		}
			
		System.out.println("*********\n");
		
		// Oval:
		
		System.out.println("   ***   ");
		
		System.out.println(" *     * ");
		
		for (int i = 0; i < 5; i++) {
			
			System.out.println("*       *");
			
		}
		
		System.out.println(" *     * ");
		
		System.out.println("   ***   \n");
		
		
		// Arrow:
		
		System.out.println("    *    ");
		
		System.out.println("   ***   ");
		
		System.out.println("  *****  ");
		
		for (int i = 0; i < 6; i++) {
			
			System.out.println("    *    \n");
			
		}
		
		
		// Diamond:
		
		System.out.println("        *        ");
		
		System.out.println("      *   *      ");
		
		System.out.println("    *       *    ");
		
		System.out.println("  *           *  ");
		
		System.out.println("    *       *    ");
		
		System.out.println("      *   *      ");
			
		System.out.println("        *        ");
	}

}